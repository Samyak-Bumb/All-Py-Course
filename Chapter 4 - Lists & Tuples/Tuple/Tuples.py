# Creating Tuples By () (Brackets)

tuple=() # Empty Tuple

# tuple=(1) # Wrong way to Declare a Tuple with Single Element (Gives an Error)

tuple=(1,) # Tuple with Only One Element but Needs Comma

tuple=(69,2,3,5) # Tuple with More than One Element

# In "Tuple" we Can't Update Data Like in "List"
# tuple[0]=69 # Gives an Error

# print(tuple[0])
print(tuple)
